                                    <p>In the early 1990s, whenever <span class="logocCr">Sangeet Martand Pandit Jasraj ji</span> and <span>Pandita Tripti Mukherjee</span> traveled to the US for music concerts, music lovers used to call on them to start a music school in the US. Although people in the US were able to attend music concerts, there were no opportunities to learn vocal classical music.</p>
                                    <p>After hearing numerous such requests, Pandit Jasraj ji and Triptiji decided to start the classes dedicated to music in the US. In September 1995, Pandit Jasraj Music Academy opened its doors to students of music on an experimental basis in New Jersey. The main goal of the Academy was to introduce music lovers to the rich treasure of Indian classical music, with a focus on the Mewati tradition. The only requirement the students had to meet was that they had a serious interest in music. Prior training in music was not necessary. To this date, the same requirement is in place for new students coming into the Institute. </p>
                                    <p>During the period of 1995 through 1999, Triptiji used to conduct music classes at two locations; New Jersey and Pittsburgh. In 1999, several music lovers in the greater New York City area started requesting for a school in New York. Considering the growing need and interest as well as a deep commitment to music in the students, Triptiji decided to put the experimental music academy on a firm footing. Thus, in the year 2000 "Pandit Jasraj School of Music Foundation (PJSMF)" was registered in the state of New York. </p>
                                </div>
                            </div>
                            <div class="row align-items-center mb-4">
                                <div class="col-md-8 tab_titl">
                                    <p>Between 2000 and 2005, PJSMF flourished through several activities including the annual Workshop, student Concerts and a publication titled "JasRangi". A clear trend was emerging in the student population as well. Pandita Tripti Mukherjee saw a segment of the student population interested in musicology and research related to the theoretical foundation of Indian music. She also noticed a group of students showing promise towards becoming performing artistes. Furthermore, a large segment of the student population demonstrated a keen interest in understanding and appreciating the nuances of Indian music. Therefore, it was decided to reorganize PJSMF to meet the needs of all the students. In January 2006, the organization was named <span class="logoCor font-weight-bold">Pandit Jasraj Institute for Music Research, Artistry and Appreciation – the Mewati Gurukul.</span><br> Pandit Jasraj Institute is a Not-for-Profit 501 (c)(3) organization. <br>The Institute continues its mission of music education and enrichment.  </p>
                                    <p>Today under the guidance of Sangeet Martand Pandit Jasraj ji and Padma Shri Pandita Tripti Mukherjee,  the initial locations of New Jersey, New York and Pensylvannia have melted the boundaries and have expanded worldwide. </p>
                                    <p>Both the faculty and students of the Institute continue to make a strong contribution to the society they live in. Through music, the Institute has brought joy, peace and harmony to the hearts of audiences throughout the United States and beyond.</p>
