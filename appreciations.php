                                <p>The recommended duration for each level above are years of continuous dedicated learning and riyaaz.</p>
                                <h6 class="mt-3 mb-3 font-weight-bold">Eligibility Criteria</h6>
                                <p>In order to be eligible for a particular level of examination,  a student must have completed an in-depth study of the entire syllabus for that level and have studied music for the recommended duration for that level. 
                                    Candidates are expected to follow the sequence of examination levels and not skip intermediate levels.
                                </p>
                                <h6 class="mt-3 mb-3 font-weight-bold">Examination Format</h6>
                                <p>The certification consists of two parts. The theory examination is a closed-book one . After the theory examination, candidates will be notified of the date for the practical examination. </p>
                                <p>Accompaniments required during the practical exam are Tanpura and Tabla. No Harmonium accompaniment is permitted. It is advisable that the candidates bring their own accompanying artiste and instruments. However, if necessary, the examiners will provide the required instruments to the candidate. No books/notes will be allowed during the practical examination. </p>
                                <h6 class="mt-3 mb-3 font-weight-bold">Scoring</h6>
                                <p>
                                    <span><b>Theory  examination:</b> 100 points.</span><br>
                                    <span><b>Practical examination:</b> 400 points.</span><br>
                                    <span><b>Cumulative Score:</b> Total 500</span><br>
                                    <span>Pass - 50%. Candidate has to pass <b>BOTH</b> the Theory and Practical examinations; </span><br>
                                    <span>Merit– 60%; </span><br>
                                    <span>Honors – 70%; </span><br>
                                    <span>Distinction – 80%; </span><br>
                                    <span>Excellence– 90%.</span>
                                </p>
                                <p>Please note that if the candidate does not pass in either section, s/he must reappear for the <b>entire examination</b> (theory and practical) to receive  a certificate.</p>
                                <p><b>Schedule and Location</b> – The dates scheduled by the Examination Board  are final. However, if for some unavoidable reason those dates are not suitable, the candidate should inform the Examination Board immediately (within a week of the exam date announcement) to arrange for a mutually workable date and time. </p>
                                <p style="color:blue;">The Theory examination will be conducted in <u>September</u> every year.</p>
                                <p style="color:blue;">The Practical examination will be conducted in <u>December </u> every year.</p>
                                <p>The location  will be communicated along with the schedule.</p>
                                <h6 class="mt-3 mb-3 font-weight-bold">Communication of Results</h6>
                                <p>
                                    Scores will be communicated to candidates after both the examinations are completed. All scores are final.  Candidates who meet the passing requirements will be awarded a certificate during the certification ceremony at our annual Sur-Laya-Chhanda Mahotsav Shibir in July. Candidates will be informed of the dates of the ceremony once finalized.
                                </p>
