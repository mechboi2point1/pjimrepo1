                            <p>Both the Theory and Practical exams require a thorough understanding of the subject so that it is a rewarding experience for both the candidate and the Examiner.</p>
                            <p>Students are advised to take their time to learn, understand, and enhance their capability,  and prepare themselves  very well from all aspects before they decide to take an Exam.</p>
                            <p>Pandit Jasraj Institute wishes all the examination candidates All the Best! <br>Jai Ho!</p>
